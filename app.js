const cal = {
  add: function (num1, num2) {
    console.log(num1 + num2);
  },
  minus: function (num1, num2) {
    console.log(num1 - num2);
  },
  multip: function (num1, num2) {
    console.log(num1 * num2);
  },
  divide: function (num1, num2) {
    console.log(num1 / num2);
  },
};

cal.add(5, 6);
cal.minus(5, 6);
cal.multip(5, 6);
cal.divide(5, 6);
